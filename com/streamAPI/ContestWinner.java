package com.streamAPI;

public class ContestWinner {

	private String name;
	private String phoneNo;

	public ContestWinner(String name, String salary) {
		
		this.name = name;
		this.phoneNo = salary;
	}

	public String getName() {
		return name;
	}

	public String getSalary() {
		return phoneNo;
	}

}
